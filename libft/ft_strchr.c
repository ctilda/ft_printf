#include "libft.h"

char	*ft_strchr(const char *s, int ch)
{
	char	*str;
	size_t	i;

	i = 0;
	str = (char *)s;
	while (str[i] != ch)
	{
		if (str[i] == '\0')
			return (0);
		i++;
	}
	return (str + i);
}
